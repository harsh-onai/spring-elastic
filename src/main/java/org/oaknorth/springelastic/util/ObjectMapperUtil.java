package org.oaknorth.springelastic.util;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.DeserializationFeature;
import com.fasterxml.jackson.databind.ObjectMapper;

public class ObjectMapperUtil {
    private static final ObjectMapper objectMapper = new ObjectMapper();

    static {
        objectMapper.configure(DeserializationFeature.FAIL_ON_UNKNOWN_PROPERTIES, false);
    }

    private ObjectMapperUtil(){

    }

    public static <T> T convertValue(Object fromValue, Class<T> destinationType) {
        return objectMapper.convertValue(fromValue, destinationType);
    }

    public static String convertJson(Object data) throws JsonProcessingException {
        return objectMapper.writeValueAsString(data);
    }

    public static <T> T convertValueFromJson(String fromValue, Class<T> destinationType)
            throws JsonProcessingException {
        return objectMapper.readValue(fromValue, destinationType);
    }
}
